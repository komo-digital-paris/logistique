// ===== APPLY LANGUAGE =====
window.applyLang = function (lang) {
  if (lang !== 'fr' && lang !== 'cn') lang = 'fr';
  document.documentElement.lang = lang === 'cn' ? 'zh-CN' : 'fr';
  document.body.dataset.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const t = (window.I18N[key] || {})[lang];
    if (t != null) el.innerHTML = t;
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.dataset.i18nPh;
    const t = (window.I18N[key] || {})[lang];
    if (t != null) el.setAttribute('placeholder', t);
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.dataset.i18nAria;
    const t = (window.I18N[key] || {})[lang];
    if (t != null) el.setAttribute('aria-label', t);
  });

  // Update toggle button visual state
  document.querySelectorAll('.lang-toggle [data-lang]').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === lang);
  });

  try { localStorage.setItem('gr-lang', lang); } catch (e) {}
};

// ===== INIT =====
document.addEventListener('DOMContentLoaded', () => {
  let saved = 'fr';
  try { saved = localStorage.getItem('gr-lang') || 'fr'; } catch (e) {}
  window.applyLang(saved);

  document.querySelectorAll('.lang-toggle [data-lang]').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      window.applyLang(btn.dataset.lang);
    });
  });
});
